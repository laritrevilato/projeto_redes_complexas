# Lê um arquivo PDB e extrai apenas os resíduos que possuem átomo Cα.
# Cada resíduo será representado como um nó da rede.

def extract_ca_residues(pdb_file, structure_id="protein"):
    parser = PDBParser(QUIET=True)
    structure = parser.get_structure(structure_id, str(pdb_file))

    data = []

    for model in structure:
        for chain in model:
            for residue in chain:
                if "CA" in residue:
                    ca = residue["CA"]
                    x, y, z = ca.coord

                    data.append({
                        "node": len(data),
                        "chain": chain.id,
                        "resname": residue.get_resname(),
                        "resid": residue.id[1],
                        "x": float(x),
                        "y": float(y),
                        "z": float(z)
                    })
        break

    return pd.DataFrame(data)

# Constrói uma rede de contatos não direcionada e não ponderada.
# Dois resíduos são conectados se a distância entre seus Cα for <= cutoff.

def build_contact_graph(df_residues, cutoff=8.0):
    G = nx.Graph()

    for _, row in df_residues.iterrows():
        G.add_node(
            int(row["node"]),
            chain=row["chain"],
            resname=row["resname"],
            resid=int(row["resid"]),
            x=row["x"],
            y=row["y"],
            z=row["z"]
        )

    coords = df_residues[["x", "y", "z"]].values
    tree = cKDTree(coords)
    pairs = tree.query_pairs(r=cutoff)

    for i, j in pairs:
        G.add_edge(i, j)

    return G

# Calcula estatísticas gerais da rede.

def calculate_graph_statistics(G):
    degrees = np.array([d for _, d in G.degree()])

    stats = {
        "nodes": G.number_of_nodes(),
        "edges": G.number_of_edges(),
        "density": nx.density(G),
        "average_degree": degrees.mean(),
        "minimum_degree": degrees.min(),
        "maximum_degree": degrees.max(),
        "median_degree": np.median(degrees),
        "connected_components": nx.number_connected_components(G),
        "largest_component": len(max(nx.connected_components(G), key=len)),
        "average_clustering": nx.average_clustering(G)
    }

    return pd.DataFrame([stats])

# Aplica o algoritmo Louvain e salva a comunidade de cada nó como atributo.

def detect_louvain(G, seed=42):
    communities = louvain_communities(G, seed=seed)

    partition = {}

    for community_id, community in enumerate(communities):
        for node in community:
            partition[node] = community_id

    nx.set_node_attributes(G, partition, "community")

    return communities, partition

# Gera histograma e gráfico log-log da distribuição de graus.

def plot_degree_distribution(G, title, output_path):
    degrees = np.array([d for _, d in G.degree()])
    unique, counts = np.unique(degrees, return_counts=True)
    pk = counts / counts.sum()

    fig, ax = plt.subplots(1, 2, figsize=(14, 5))

    ax[0].hist(degrees, bins=30, edgecolor="black")
    ax[0].set_title("Histograma dos graus")
    ax[0].set_xlabel("Grau")
    ax[0].set_ylabel("Frequência")

    ax[1].scatter(unique, pk, s=40)
    ax[1].set_xscale("log")
    ax[1].set_yscale("log")
    ax[1].set_xlabel("Grau k")
    ax[1].set_ylabel("P(k)")
    ax[1].set_title("Distribuição em escala log-log")
    ax[1].grid(alpha=0.3)

    plt.suptitle(title)
    plt.tight_layout()
    plt.savefig(output_path, dpi=300, bbox_inches="tight")
    plt.show()

# Plota a rede em 2D colorida pelas comunidades.

def plot_community_graph(G, partition, title, output_path):
    colors = [partition[node] for node in G.nodes()]
    pos = nx.spring_layout(G, seed=42)

    plt.figure(figsize=(8, 8))
    nx.draw_networkx_nodes(
        G,
        pos,
        node_color=colors,
        cmap=plt.cm.tab20,
        node_size=80
    )
    nx.draw_networkx_edges(G, pos, alpha=0.3)
    plt.axis("off")
    plt.title(title)

    plt.savefig(output_path, dpi=300, bbox_inches="tight")
    plt.show()

