# Testa diferentes valores de cutoff para avaliar o impacto
# na densidade da rede, modularidade e número de comunidades.

def test_cutoffs(df_residues, cutoffs, seed=42):
    results = []

    for cutoff in cutoffs:
        print(f"Testando cutoff = {cutoff} Å")
        start = time.time()

        G_temp = build_contact_graph(df_residues, cutoff=cutoff)
        communities_temp, partition_temp = detect_louvain(G_temp, seed=seed)
        mod = modularity(G_temp, communities_temp)

        degrees = np.array([d for _, d in G_temp.degree()])

        results.append({
            "cutoff": cutoff,
            "nodes": G_temp.number_of_nodes(),
            "edges": G_temp.number_of_edges(),
            "avg_degree": degrees.mean(),
            "max_degree": degrees.max(),
            "num_communities": len(communities_temp),
            "modularity": mod,
            "time_seconds": time.time() - start
        })

    return pd.DataFrame(results)

